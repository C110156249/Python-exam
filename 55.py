A = ["飢餓遊戲3","解憂雜貨店","怪獸與牠們的產地","哈利波特6","我的阿富汗筆友","祈念之樹","樓下的房客","小王子"]
B = ["房思琪的初戀樂園","等一個人咖啡","鬼滅之刃14","神農嘗百草","麥田捕手","老人與海","傲慢與偏見","與神同行"]
a = str(input("輸入欲租借的書籍:"))
n = A.count(a)
m = B.count(a)
if n > 0 or m > 0:
    for i in range(8):        
        if a == A[i]:
            print("在書架A的第",i+1,"本")
        elif a == B[i]:
            print("在書架B的第",i+1,"本")
else:
    print("查無此書籍")